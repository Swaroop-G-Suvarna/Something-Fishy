package info.accolade.fishing_master.modal;

public class SearchModal {
    private  String boatname;
    private  String boatno;
    private  String fishname;
    private  String arrivingdate;
    private  String createddate;
    private  String image;
    private  String description;

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getBoatname() {
        return boatname;
    }

    public void setBoatname(String boatname) {
        this.boatname = boatname;
    }

    public String getBoatno() {
        return boatno;
    }

    public void setBoatno(String boatno) {
        this.boatno = boatno;
    }

    public String getFishname() {
        return fishname;
    }

    public void setFishname(String fishname) {
        this.fishname = fishname;
    }

    public String getArrivingdate() {
        return arrivingdate;
    }

    public void setArrivingdate(String arrivingdate) {
        this.arrivingdate = arrivingdate;
    }

    public String getCreateddate() {
        return createddate;
    }

    public void setCreateddate(String createddate) {
        this.createddate = createddate;
    }
}
